        public override bool ValidateUser(string ID, string password)
        {
            int count = 0;
            bool result = false;
            
            if (ID != null &&  password != null)
            {
                string CS = ConfigurationManager.ConnectionStrings["cnstr"].ConnectionString;

                string query = "";
                using (SqlConnection con = new SqlConnection(CS))
                {
                    query = "";
                    query = "SELECT Count(*) Cnt From tblUser Where ID = @ID and password = @password and DELFG = 0";

                    SqlCommand cmd = new SqlCommand(query, con);
                    cmd.Parameters.AddWithValue("@ID", ID);
                    cmd.Parameters.AddWithValue("@password", password);
                    cmd.CommandType = CommandType.Text;
                    con.Open();
                    SqlDataReader rdr = cmd.ExecuteReader();
                    while (rdr.Read())
                    {
                        count = Convert.ToInt32(rdr["Cnt"]);

                    }
                }
                if (count == 1)
                {
                    result = true;
                }
            }
            
            return result;
        }
